	int yesOrNoDesign()
	{
		CursorControl cursor;
		cursor.gotoxy(49, 12);
		std::cout << "┌───────┐";
		cursor.gotoxy(48, 13);
		std::cout << ">│   네  │";
		cursor.gotoxy(49, 14);
		std::cout << "└───────┘";
		cursor.gotoxy(62, 12);
		std::cout << "┌───────┐";
		cursor.gotoxy(62, 13);
		std::cout << "│ 아니오│";
		cursor.gotoxy(62, 14);
		std::cout << "└───────┘ ";
		int choice = 0;
		while (1)
		{
			int input;
			if (_kbhit())
			{
				input = _getch();
				switch (input)
				{
				case LEFT:
					if (choice > 0)
					{
						choice--;
					}
					cursor.gotoxy(61, 13);
					std::cout << " ";
					cursor.gotoxy(48, 13);
					std::cout << ">";
					break;
				case RIGHT:
					if (choice < 1)
					{
						choice++;
					}
					cursor.gotoxy(48, 13);
					std::cout << " ";
					cursor.gotoxy(61, 13);
					std::cout << ">";
					break;
				case SPACE:
					return choice;
				}
			}
		}
	}

